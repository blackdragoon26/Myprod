type RenderedFile struct {
	Path    string
	Content string
}

func ControlPlaneNode(cfg Config) (Node, bool) {
	for _, node := range cfg.Nodes {
		if node.Role == "control-plane" {
			return node, true
		}
	}
	return Node{}, false
}

func RenderControlPlane(cfg Config) ([]RenderedFile, error) {
	node, ok := ControlPlaneNode(cfg)
	if !ok {
		return nil, fmt.Errorf("config has no control-plane node")
	}
	if node.OverlayIP == "" {
		return nil, fmt.Errorf("control-plane node %q is missing overlay_ip", node.Name)
	}
	if node.PublicIP == "" {
		return nil, fmt.Errorf("control-plane node %q is missing public_ip", node.Name)
	}
	if node.SSHUser == "" {
		return nil, fmt.Errorf("control-plane node %q is missing ssh_user", node.Name)
	}
	if node.SSHKey == "" {
		return nil, fmt.Errorf("control-plane node %q is missing ssh_key", node.Name)
	}

	files := []RenderedFile{
		{Path: "bootstrap-control-plane.sh", Content: renderBootstrapScript(node)},
		{Path: "nomad/server.hcl", Content: renderNomadServer(node)},
		{Path: "systemd/nomad.service", Content: renderNomadService()},
		{Path: "traefik/traefik.yml", Content: renderTraefikStatic(node)},
		{Path: "systemd/traefik.service", Content: renderTraefikService()},
		{Path: "wireguard/wg0.conf", Content: renderWireGuard(node)},
		{Path: "systemd/poolctl-guard.service", Content: renderGuardService()},
		{Path: "systemd/poolctl-guard.timer", Content: renderGuardTimer()},
	}
	return files, nil
}

func RenderAppJob(cfg Config, appName string) (RenderedFile, error) {
	app, ok := cfg.FindApp(appName)
	if !ok {
		return RenderedFile{}, fmt.Errorf("unknown app %q", appName)
	}
	if app.Image == "" {
		return RenderedFile{}, fmt.Errorf("app %q is missing image", app.Name)
	}
	if app.Domain == "" {
		return RenderedFile{}, fmt.Errorf("app %q is missing domain", app.Name)
	}
	if app.Port == 0 {
		return RenderedFile{}, fmt.Errorf("app %q is missing port", app.Name)
	}
	target, ok := cfg.FindNode(app.PreferNode)
	if !ok {
		return RenderedFile{}, fmt.Errorf("app %q targets unknown node %q", app.Name, app.PreferNode)
	}
	if target.OverlayIP == "" {
		return RenderedFile{}, fmt.Errorf("target node %q is missing overlay_ip", target.Name)
	}

	return RenderedFile{
		Path:    filepath.Join("nomad", "jobs", app.Name+".nomad.hcl"),
		Content: renderNomadJob(app),
	}, nil
}

func RenderWorkerJoin(control, worker Node, controlPublicKey string) ([]RenderedFile, error) {
	if control.OverlayIP == "" {
		return nil, fmt.Errorf("control-plane node %q is missing overlay_ip", control.Name)
	}
	if control.PublicIP == "" {
		return nil, fmt.Errorf("control-plane node %q is missing public_ip", control.Name)
	}
	if worker.Name == "" {
		return nil, fmt.Errorf("worker node is missing name")
	}
	if worker.OverlayIP == "" {
		return nil, fmt.Errorf("worker node %q is missing overlay_ip", worker.Name)
	}
	if worker.PublicIP == "" {
		return nil, fmt.Errorf("worker node %q is missing public_ip", worker.Name)
	}
	if controlPublicKey == "" {
		return nil, fmt.Errorf("control-plane WireGuard public key is missing")
	}

	return []RenderedFile{
		{Path: "bootstrap-worker.sh", Content: renderWorkerBootstrapScript(control, worker, controlPublicKey)},
		{Path: "nomad/client.hcl", Content: renderNomadClient(control, worker)},
		{Path: "systemd/nomad.service", Content: renderNomadService()},
	}, nil
}

func WriteRendered(base string, files []RenderedFile) error {
	for _, file := range files {
		target := filepath.Join(base, file.Path)
		if err := os.MkdirAll(filepath.Dir(target), 0o755); err != nil {
			return err
		}
		mode := os.FileMode(0o644)
		if strings.HasSuffix(file.Path, ".sh") {
			mode = 0o755
		}
		if err := os.WriteFile(target, []byte(file.Content), mode); err != nil {
			return err
		}
	}
	return nil
}

func CopyLocalBinaryIntoBundle(base string) (bool, error) {
	const localBinary = "work/poolctl"
	data, err := os.ReadFile(localBinary)
	if err != nil {
		if os.IsNotExist(err) {
			return false, nil
		}
		return false, err
	}
	target := filepath.Join(base, "poolctl")
	if err := os.WriteFile(target, data, 0o755); err != nil {
		return false, err
	}
	return true, nil
}

