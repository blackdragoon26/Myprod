func renderWorkerBootstrapScript(control, worker Node, controlPublicKey string) string {
	return fmt.Sprintf(`#!/usr/bin/env bash
set -Eeuo pipefail

# Generated by poolctl for worker %s.

NOMAD_VERSION="${NOMAD_VERSION:-%s}"
CONTROL_PUBLIC_IP="%s"
CONTROL_OVERLAY_IP="%s"
WORKER_OVERLAY_IP="%s"
CONTROL_WG_PUBLIC_KEY="%s"

die() {
  echo "ERROR: $*" >&2
  exit 1
}

trap 'echo "ERROR: worker bootstrap failed near line $LINENO: $BASH_COMMAND" >&2' ERR

require_root() {
  if [ "$(id -u)" -eq 0 ]; then
    SUDO=""
  else
    SUDO="sudo"
  fi
}

detect_arch() {
  case "$(uname -m)" in
    aarch64|arm64) echo "arm64" ;;
    x86_64|amd64) echo "amd64" ;;
    *) die "unsupported architecture: $(uname -m)" ;;
  esac
}

install_zip_binary() {
  name="$1"
  version="$2"
  url="$3"
  sums_url="$4"
  archive="$5"
  binary="$6"

  tmp_dir="$(mktemp -d)"
  trap 'rm -rf "$tmp_dir"' RETURN

  curl -fsSL "$url" -o "$tmp_dir/$archive"
  curl -fsSL "$sums_url" -o "$tmp_dir/SHA256SUMS"
  (cd "$tmp_dir" && grep "  $archive$" SHA256SUMS | sha256sum -c -) || die "$name checksum verification failed"
  unzip -q "$tmp_dir/$archive" -d "$tmp_dir/$name"
  $SUDO install -m 0755 "$tmp_dir/$name/$binary" "/usr/local/bin/$name"
  "$name" version || true
}

render_wireguard_config() {
  $SUDO install -d -m 0700 /etc/wireguard
  if [ ! -f /etc/wireguard/poolctl-worker.key ]; then
    wg genkey | $SUDO tee /etc/wireguard/poolctl-worker.key >/dev/null
    $SUDO chmod 0600 /etc/wireguard/poolctl-worker.key
  fi
  private_key="$($SUDO cat /etc/wireguard/poolctl-worker.key)"
  $SUDO install -m 0600 /dev/null /etc/wireguard/wg0.conf
  $SUDO tee /etc/wireguard/wg0.conf >/dev/null <<WGEOF
[Interface]
Address = $WORKER_OVERLAY_IP/24
ListenPort = 51820
PrivateKey = $private_key

[Peer]
# %s
PublicKey = $CONTROL_WG_PUBLIC_KEY
AllowedIPs = $CONTROL_OVERLAY_IP/32
Endpoint = $CONTROL_PUBLIC_IP:51820
PersistentKeepalive = 25
WGEOF
  $SUDO systemctl enable wg-quick@wg0
  $SUDO systemctl restart wg-quick@wg0
}

wait_for_wireguard() {
  if [ "${POOLCTL_SKIP_WG_WAIT:-}" = "1" ]; then
    echo "Skipping WireGuard reachability wait; poolctl will verify after control-plane peer update."
    return
  fi
  for _ in $(seq 1 30); do
    if ping -c 1 -W 1 "$CONTROL_OVERLAY_IP" >/dev/null 2>&1; then
      return
    fi
    sleep 2
  done
  echo "---- worker wg show ----"
  $SUDO wg show || true
  echo "---- worker wg0 config ----"
  $SUDO sed -n '1,120p' /etc/wireguard/wg0.conf | sed 's/PrivateKey = .*/PrivateKey = <redacted>/' || true
  echo "---- worker wg0 address ----"
  ip addr show wg0 || true
  echo "---- worker route to control overlay ----"
  ip route get "$CONTROL_OVERLAY_IP" || true
  echo "---- worker public route to control public IP ----"
  ip route get "$CONTROL_PUBLIC_IP" || true
  die "cannot reach control plane WireGuard IP $CONTROL_OVERLAY_IP"
}

require_root
ARCH="$(detect_arch)"

$SUDO apt-get update
$SUDO env DEBIAN_FRONTEND=noninteractive apt-get install -y curl unzip ca-certificates gnupg lsb-release wireguard openssl tar ufw

if ! command -v docker >/dev/null 2>&1; then
  curl -fsSL https://get.docker.com | $SUDO sh
  $SUDO usermod -aG docker %s
fi

$SUDO useradd --system --home /etc/nomad.d --shell /usr/sbin/nologin nomad 2>/dev/null || true

NOMAD_ARCHIVE="nomad_${NOMAD_VERSION}_linux_${ARCH}.zip"
NOMAD_URL="https://releases.hashicorp.com/nomad/${NOMAD_VERSION}/${NOMAD_ARCHIVE}"
NOMAD_SUMS_URL="https://releases.hashicorp.com/nomad/${NOMAD_VERSION}/nomad_${NOMAD_VERSION}_SHA256SUMS"
install_zip_binary "nomad" "$NOMAD_VERSION" "$NOMAD_URL" "$NOMAD_SUMS_URL" "$NOMAD_ARCHIVE" "nomad"

render_wireguard_config
wait_for_wireguard

$SUDO install -d -m 0750 -o nomad -g nomad /etc/nomad.d /etc/nomad.d/tls /opt/nomad
$SUDO install -m 0640 -o nomad -g nomad nomad/client.hcl /etc/nomad.d/client.hcl
$SUDO install -m 0644 systemd/nomad.service /etc/systemd/system/nomad.service
$SUDO install -m 0644 -o nomad -g nomad tls/nomad-agent-ca.pem /etc/nomad.d/tls/nomad-agent-ca.pem
$SUDO install -m 0644 -o nomad -g nomad tls/global-client-nomad.pem /etc/nomad.d/tls/global-client-nomad.pem
$SUDO install -m 0640 -o nomad -g nomad tls/global-client-nomad-key.pem /etc/nomad.d/tls/global-client-nomad-key.pem

$SUDO ufw allow OpenSSH
$SUDO ufw allow in on wg0 from 10.44.0.0/24 to any port 20000:32000 proto tcp
$SUDO ufw --force enable

$SUDO systemctl daemon-reload
$SUDO systemctl enable nomad
$SUDO systemctl restart nomad

echo "Worker: %s"
echo "Worker overlay IP: $WORKER_OVERLAY_IP"
echo "Control overlay IP: $CONTROL_OVERLAY_IP"
echo "Done. Run on control plane: nomad node status"
`, worker.Name, defaultNomadVersion, control.PublicIP, control.OverlayIP, worker.OverlayIP, controlPublicKey, control.Name, userOrUbuntu(worker), worker.Name)
}

